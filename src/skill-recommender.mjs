import { PatternDetector } from './pattern-detector.mjs';

/**
 * Recommends skills based on insights and code patterns.
 */
export function recommendSkills(insights, patterns, { topN = 10 } = {}) {
  const detector = new PatternDetector();
  const result = detector.scoreOpportunities(patterns, insights);

  return {
    skills: result.opportunities.slice(0, topN),
    totalScored: result.totalScored,
    recommendation: result.recommendation,
  };
}

/**
 * Generates a markdown report for the recommended skills.
 */
export function generateReport(skills, metadata) {
  let report = `# Obsidian Vault Mining — Weekly Report\n`;
  report += `Generated: ${new Date().toISOString().split('T')[0]}\n\n`;

  report += `## Top ${skills.length} Skill Ideas (by ROI)\n\n`;

  for (const s of skills) {
    report += `${s.rank}. **${s.skill}** (Quality Score: ${s.score}/10)\n`;
    report += `   - Source: ${s.source}\n`;
    report += `   - Impact: ${s.impact}/10, Maturity: ${s.maturity}/10, Complexity: ${s.complexity}/10\n`;
    report += `   - ROI Estimate: ${s.estimatedROI}\n`;
    report += `   - Risk: ${s.riskLevel}\n`;
    if (s.details && s.details.pain) {
      report += `   - Context: ${s.details.pain} (${s.details.frequency} occurrences)\n`;
    } else if (s.details && s.details.usageCount) {
      report += `   - Context: Found in ${s.details.usageCount} note(s) across ${s.details.files.length} unique file(s)\n`;
    }
    report += `\n`;
  }

  report += `## Metrics\n\n`;
  report += `- Total notes analyzed: ${metadata.totalNotes}\n`;
  report += `- Overall vault quality: ${Math.round(metadata.overallQuality * 100)}%\n`;
  report += `- Top pain points detected: ${metadata.topPains.join(', ')}\n\n`;

  report += `---\n*Generated by vault-mining-scheduler.sh*\n`;

  return report;
}

/**
 * Generates a JSON report for the recommended skills.
 */
export function generateJSONReport(result) {
  return JSON.stringify({
    timestamp: new Date().toISOString(),
    ...result
  }, null, 2);
}
